$(document).ready(function(){
  $(".thumbnail1").mouseenter(function(){
    $('#diz').stop().show();
  });

  $(".thumbnail1").mouseleave(function(){
        if(!$('#diz').is(':hover')){
          $('#diz').hide();
        };
      });
});

